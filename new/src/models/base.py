"""
VLM Model Abstractions
======================
Abstract base class, configuration dataclass, and result dataclass for all
VLM backends (HuggingFace open-weight models and closed-source API models).

The key design decision is that ``VLMModel`` exposes a single unified interface
regardless of backend. Model-specific logic (chat templates, image encoding,
API auth) lives entirely inside each subclass.
"""

from __future__ import annotations

import abc
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Optional

import yaml
from PIL import Image


# ---------------------------------------------------------------------------
# ModelConfig
# ---------------------------------------------------------------------------


@dataclass
class ModelConfig:
    """Declarative configuration for one VLM backend.

    Loaded from a YAML file in ``configs/models/``.

    Parameters
    ----------
    model_id:
        Canonical identifier. For HF models: HuggingFace repo id
        (e.g. ``"google/medgemma-4b-it"``). For API models: the API model
        string (e.g. ``"gpt-4o-2024-11-20"``).
    display_name:
        Short human-readable name used in result DataFrames and figures
        (e.g. ``"MedGemma-4B"``, ``"GPT-4o"``). This goes into the
        ``model_name`` column — not the raw model_id.
    backend:
        Selects the VLMModel subclass. One of:
        ``"hf_medgemma"``, ``"hf_llava_med"``, ``"hf_qwen25_vl"``,
        ``"openai"``, ``"anthropic"``, ``"gemini"``.
    max_new_tokens:
        Maximum tokens to generate. 256 for label-only prompts; 512+ for CoT.
    inference_params:
        Backend-specific generation kwargs. For HF: ``{"do_sample": False}``.
        For API: ``{"temperature": 0, "seed": 42}``.
    hf_load_in_4bit:
        HF only. Enable 4-bit bitsandbytes quantisation (reduces VRAM).
    hf_device:
        HF only. ``"auto"`` uses accelerate device_map; pass ``"cuda:0"`` to
        force a specific device.
    cost_per_1k_input_tokens:
        API only. USD cost per 1K input tokens. Used by CostTracker.
    cost_per_1k_output_tokens:
        API only. USD cost per 1K output tokens. Used by CostTracker.
    """

    model_id: str
    display_name: str
    backend: str
    max_new_tokens: int = 512
    inference_params: dict[str, Any] = field(default_factory=dict)

    # HF-specific
    hf_load_in_4bit: bool = False
    hf_device: str = "auto"

    # API-specific
    cost_per_1k_input_tokens: float = 0.0
    cost_per_1k_output_tokens: float = 0.0

    @classmethod
    def from_yaml(cls, path: str | Path) -> "ModelConfig":
        """Load a ModelConfig from a YAML file."""
        with open(path) as f:
            data = yaml.safe_load(f)
        return cls(
            model_id=data["model_id"],
            display_name=data["display_name"],
            backend=data["backend"],
            max_new_tokens=data.get("max_new_tokens", 512),
            inference_params=data.get("inference_params", {}),
            hf_load_in_4bit=data.get("hf_load_in_4bit", False),
            hf_device=data.get("hf_device", "auto"),
            cost_per_1k_input_tokens=data.get("cost_per_1k_input_tokens", 0.0),
            cost_per_1k_output_tokens=data.get("cost_per_1k_output_tokens", 0.0),
        )


# ---------------------------------------------------------------------------
# PredictResult
# ---------------------------------------------------------------------------


@dataclass
class PredictResult:
    """Typed result from a single ``VLMModel.predict()`` call.

    Separates raw output capture from label parsing. The label parser in
    ``evaluation/label_parser.py`` consumes ``raw_output`` to fill
    ``predicted_label`` in the final results DataFrame.

    Parameters
    ----------
    raw_output:
        The complete string generated by the model (before label parsing).
    latency_s:
        Wall-clock time for the inference call in seconds.
    input_tokens:
        Number of input tokens. For API models: from response.usage.
        For HF models: inputs["input_ids"].shape[1].
    output_tokens:
        Number of output tokens generated.
    cost_usd:
        Estimated cost in USD. 0.0 for HF (local) models.
    error:
        Exception message if inference failed; None on success.
    model_id:
        Echoed from the model for tracing.
    """

    raw_output: str
    latency_s: float
    input_tokens: int = 0
    output_tokens: int = 0
    cost_usd: float = 0.0
    error: Optional[str] = None
    model_id: str = ""


# ---------------------------------------------------------------------------
# VLMModel (abstract base)
# ---------------------------------------------------------------------------


class VLMModel(abc.ABC):
    """Abstract interface that all VLM backends must implement.

    Every subclass wraps exactly one model and exposes a unified
    ``predict()`` / ``predict_batch()`` interface. The subclass is
    responsible for image pre-processing, prompt formatting, and output
    decoding — so callers never need model-specific logic.

    Usage (recommended — ensures load/unload even on exceptions)::

        config = ModelConfig.from_yaml("configs/models/medgemma_4b.yaml")
        model = build_model(config)
        with model:
            results = model.predict_batch(images, prompts)

    Parameters
    ----------
    config:
        :class:`ModelConfig` instance describing this model.
    """

    def __init__(self, config: ModelConfig) -> None:
        self.config = config

    # ------------------------------------------------------------------
    # Identity / metadata
    # ------------------------------------------------------------------

    @property
    def model_id(self) -> str:
        """HF repo id or API model string."""
        return self.config.model_id

    @property
    def display_name(self) -> str:
        """Short human-readable name for DataFrames and figures."""
        return self.config.display_name

    @property
    def backend(self) -> str:
        """Backend identifier string (e.g. ``"hf_medgemma"``, ``"openai"``)."""
        return self.config.backend

    @property
    def is_api_model(self) -> bool:
        """True if this model calls an external API (no local GPU needed)."""
        return self.backend in {"openai", "anthropic", "gemini"}

    # ------------------------------------------------------------------
    # Core inference interface (must implement)
    # ------------------------------------------------------------------

    @abc.abstractmethod
    def predict(self, image: Image.Image, prompt: str) -> PredictResult:
        """Run inference on a single (image, prompt) pair.

        Parameters
        ----------
        image:
            PIL RGB image. The implementation handles all pre-processing.
        prompt:
            Fully-formed prompt string (one of the four CHD strategies).

        Returns
        -------
        PredictResult
            Contains raw_output, latency_s, token counts, and any error.
        """
        ...

    @abc.abstractmethod
    def predict_batch(
        self,
        images: list[Image.Image],
        prompts: list[str],
    ) -> list[PredictResult]:
        """Run inference on a batch of (image, prompt) pairs.

        For HF models: enables GPU batching (significant throughput improvement).
        For API models: runs sequential predict() calls — true parallelism is
        handled upstream in EvaluationRunner via ThreadPoolExecutor.

        Parameters
        ----------
        images:
            List of PIL RGB images. Length must equal ``len(prompts)``.
        prompts:
            List of prompt strings, one per image.

        Returns
        -------
        list[PredictResult]
            One result per input pair, in the same order.
        """
        ...

    # ------------------------------------------------------------------
    # Lifecycle hooks (override in subclasses)
    # ------------------------------------------------------------------

    def load(self) -> None:
        """Load model weights into memory.

        For HF models: download and load to GPU.
        For API models: validate API key and instantiate client.
        Called automatically by the context manager (``__enter__``).
        """

    def unload(self) -> None:
        """Release model weights from memory.

        For HF models: ``del model``, ``gc.collect()``, ``torch.cuda.empty_cache()``.
        For API models: no-op.
        Called automatically by the context manager (``__exit__``).
        """

    # ------------------------------------------------------------------
    # Context manager (auto load / unload)
    # ------------------------------------------------------------------

    def __enter__(self) -> "VLMModel":
        self.load()
        return self

    def __exit__(self, exc_type: Any, exc_val: Any, exc_tb: Any) -> None:
        self.unload()

    def __repr__(self) -> str:
        return f"{self.__class__.__name__}(display_name={self.display_name!r}, backend={self.backend!r})"
